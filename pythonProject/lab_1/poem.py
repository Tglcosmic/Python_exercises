tu_song = "Sống"
#poem
poem = f'''
\t"...
\t{tu_song} không giận, không hờn không oán trách
\t{tu_song} mỉm cười, với thử thách chông gai
\t{tu_song} vươn lên, theo kịp ánh ban mai
\t{tu_song} chan hòa, với những người chung sống.\n
\t{tu_song} là động, nhưng lòng luôn bất động
\t{tu_song} là thương, nhưng lòng chẳng vấn vương
\t{tu_song} yên vui, danh lại mãi coi thường
\tTâm bất biến, giữ dòng đời vạn biến
\t..."
'''

print(poem)
