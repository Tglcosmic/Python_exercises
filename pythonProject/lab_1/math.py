#nhập dữ liệu (input)
chieu_dai = float(input("chiều dài của hình chữ nhật là: "))
chieu_rong = float(input("chiều rộng của hình chữ nhật là: "))

#công thức tính (caculator)
chu_vi = (chieu_dai + chieu_rong) * 2
dien_tich = chieu_dai * chieu_rong

#kết quả (output)
print(f"chu vi của hình chữ nhật: {chu_vi}")
print(f"diện tích của hình chữ nhật: {dien_tich}")